// 2. WAP to find sum of two numbers

#include <stdio.h>

int main()
{
    int a, b;
    // Taking input for number
    printf("Enter number a = ");
    scanf("%d", &a);

    printf("Enter number b = ");
    scanf("%d", &b);

    // Printing sum
    printf("The sum is %d\n", a + b);

    return 0;
}